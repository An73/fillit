#include "fillit.h"

void	ft_print(t_field *finish, int len_)
{
	int		i;

	i = 0;
	while (i < len_ * len_)
	{
		write(1, &finish[i].point, 1);
		if((i + 1) % len_ == 0)
			write(1, "\n", 1);
		i++;
	}
}
